    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int result = validateInput();
        if(result == 0){
            JOptionPane.showMessageDialog(null, "Please input numeric value for salary","Invalid Input!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(result==-1){
            JOptionPane.showMessageDialog(null, "Please input employee name","Invalid Input!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            Employee e = new Employee();
            e.setEmpId(txtEmpId.getText());
            e.setEmpName(empName);
            e.setJob(jcJob.getSelectedItem().toString());
            e.setSalary(salary);
            boolean status = EmpDao.addEmployee(e);
            if(status){
                JOptionPane.showMessageDialog(null, "Employee Successfully added","Success!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            }
            else{
                JOptionPane.showMessageDialog(null, "Cannot add the Employee","Adding Employee denied!",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "Error while adding employee","Exception!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

