    private void jcEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmpIdActionPerformed
        try{
            String empId = jcEmpId.getSelectedItem().toString();
            employeeList = EmpDao.getEmployeesByEmpId(empId);
            jcJob.removeAllItems();
        Employee e=employeeList.get(empId);
        txtEmpName.setText(e.getEmpName());
        jcJob.addItem(e.getJob());
        txtSalary.setText(String.valueOf(e.getSalary()));
        }
        catch(SQLException ex){
             JOptionPane.showMessageDialog(null, "Error while connection to DB","Exception!",JOptionPane.ERROR_MESSAGE);
             ex.printStackTrace();
        }
    }//GEN-LAST:event_jcEmpIdActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int result = validateInput();
        if(result == 0){
            JOptionPane.showMessageDialog(null, "Please input numeric value for salary","Invalid Input!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(result==-1){
            JOptionPane.showMessageDialog(null, "Please input employee name","Invalid Input!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            e.setEmpId(empId);
            e.setJob(job);
            e.setEmpName(empName);
            e.setSalary(salary);
            boolean status = EmpDao.updateEmployee(e);
            if(status){
                JOptionPane.showMessageDialog(null, "Employee Successfully updated","Success!",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(null, "Employee not updated!","Updation denied!",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "Error while updating Employee!","Exception!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

